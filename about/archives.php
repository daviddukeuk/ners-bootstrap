	<h2>The progression of Rattitude</h2>
	<p>The first issue of the NERS newsletter <a href="/rattitude">Rattitude</a> was edited by Alison Campbell and published in July 2001. It was 24 pages long with a large font size (already century gothic). The front cover carried a picture drawn by Lizzi McIntee, and a copy of the original society logo. It was initially produced monthly, albeit with much less content. Towards the end of 2002 the committee decided to produce a bi-monthly magazine as they were beginning to reduce the number of shows held each year from the original 12. In 2002-03 the front cover carried a picture drawn by Gary Gibson, the font size was much smaller, and (by 2003) the number of pages had grown to 36.</p>

	<p>From the 2004 AGM, Clare Ashworth took over as editor and gave the newsletter a new look. The cover carried a series of line-drawn rats mostly by Clare herself. Size and general formatting remained the same.</p>

	<p>toyah Leitch (n√©e Robson) took over the role of editor at the 2006 AGM, and again revamped the appearance of the magazine. Alison Campbell rejoined her on the team as the person responsible for collating content. The magazine took on a new 'clean' look with the NERS logo replacing the cover picture and the first use of columns for the articles. Font size was also reduced further, allowing for a slightly smaller 32-page magazine. A cover colour was chosen to run for each calendar year.</p>

    
	
	
	
	
