<h2>NERS constitution and rules</h2>
<p>Membership of the North of England Rat Society is open to all who are in sympathy with the aims and objectives of the Society as reflected by the constitution:</p>
<ul>
	<li>To uphold and encourage the fancy rat as an excellent pet and exhibition animal.</li>
	<li>To safeguard the interests of the fancy rat, and support the rat fancy in general.</li>
	<li>To advocate and champion the highest quality of rat care.</li>
	<li>To advance safe and reputable breeding practices.</li>
	<li>To adopt and promote the NFRS 'standards of excellence', by which fancy rats should be judged.</li>
	<li>To encourage and maintain optimum practice in judging and show management.</li>
	<li>To provide help and support for rat keepers throughout the North of England and surrounding areas.</li>
</ul>
<p>The society rules are issued to all new members and can be viewed or downloaded <a href="/resources/docs/societyrules17-18.pdf" target="_blank">here</a>.</p>

<h2>NERS history</h2>
<p>The NERS was set up in 2001 by a diverse group of ratters who saw the need for a club based in the North of England that was democratic and represented the interests of its membership. Members of the original NERS committee were:</p>
<ul>
	<li>Chair: Alison Campbell</li>
	<li>Secretary: Stephanie Gray</li>
	<li>Treasurer: Jenny Litten</li>
	<li>Show Secretary: Linda Collins</li>
	<li>Membership: toyah Robson</li>
	<li>Publicity/Webmaster: Estelle Sandford</li>
	<li>Newsletter: Alison Campbell and Stephanie Gray</li>
	<li>Judges Co-ordinator: Sheila Sowter</li>
	<li>Committee members: Kelly Harrison and Karen Smart</li>
</ul>
<p>Colin Arundel was also heavily involved in the early days and soon joined the committee. The NERS is extremely grateful for the enthusiasm, vision and sheer hard work given to the club by these individuals in the first two years. They laid the foundations for all that has happened since.</p>
<p>The NERS has grown and developed since its conception in 2001 and is now an established, thriving club, which has the   well-being of rats at the very centre of all that it undertakes. Significant developments over recent years have been the <a href="/rescuefund" title="NERS Rescue Fund" target="_self"> Rescue Fund</a> and our  Award Scheme, particularly the <a href="/longevity" title="NERS Longevity Award scheme" target="_self"> Longevity award scheme</a>, which promotes and rewards excellence in the breeding and care of rats.</p>

<p><img src="/resources/pics/firstNERSshow.jpg" class="col img-responsive" style="max-width:500px;" alt="Picture of show tanks on tables at the first NERS show." title="The first NERS show"></p>

<p>Initially the club was based in Newcastle-upon-Tyne, with monthly shows at the Longbenton venue. When this venue became unavailable we moved to Monkchester Community Centre and decided to diversify to other areas of the North. Over the years the society has held shows in Preston, Carlisle, Bradford and Featherstone, whilst continuing to meet at least twice a year in Newcastle. We have also been involved in other promotional events in the region, such as providing a 'meet the rats' event at the Cleveland show. We have maintained links with the <a href="http://www.nfrs.org/" title="National Fancy Rat Society" target="_blank">National Fancy Rat Society</a>, primarily through joint show ventures but more recently in applying for star status for two of our shows each year.

<p>Like all organisations, the NERS has developed and evolved from its 'baby' beginnings. Many aspects of the club have been expanded and improved as vision has grown. Some of these achievements over time are reflected in our small (but growing) archives. A few are <a href="/about/archives" title="Archives" target="_self">expanded here</a>.</p>
