	<h2>Welcome to the North of England Rat Society (NERS)</h2>
	<p>The society was birthed in June 2001 by a diverse group of ratters who were keen to see a democratic rat club representing the interests of rat lovers in the North of England. The society, whilst serious about the rat as an exhibition animal, has always maintained a heavy bias towards the rat as a splendid companion animal. This is reflected in our constitution: </p>
	<ul>
		<li>To uphold and encourage the fancy rat as an excellent pet and exhibition animal.</li>
		<li>To safeguard the interests of the fancy rat, and support the rat fancy in general.</li>
		<li>To advocate and champion the highest quality of rat care.</li>
		<li>To advance safe and reputable breeding practices.</li>
		<li>To adopt and promote the NFRS 'standards of excellence', by which fancy rats should be judged.</li>
		<li>To encourage and maintain optimum practice in judging and show management.</li>
		<li>To provide help and support for rat keepers throughout the North of England and surrounding areas.</li>
	</ul>
	<div class="row">
