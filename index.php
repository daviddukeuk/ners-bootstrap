			<p>Today our membership continues to grow, and as well as regular shows around the region we also produce a quarterly magazine which is packed full of informative and fun rat-related articles. Our shows are not just an event for people who want to exhibit fancy rats, but a warm and generous environment where all those who are enthralled by rats can gather to celebrate them, learn more about them and stock up on rat-related goodies from our shop. We provide catering, children's activities, fun events for the rats, a range of pet classes and a full variety schedule.</p>
