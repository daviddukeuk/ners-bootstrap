<h2>Our Magazine: Rattitude</h2>

<p>The society produces a quarterly, full-colour 32 page A5 magazine called Rattitude, which aims to appeal to all of our diverse membership. We try to maintain a balance between information about the society, features about our members and their rats, research-based articles about rat behaviour and care, and items of interest such as book/equipment reviews. We aim for a standard that will be interesting whether you have been involved with rats for a few weeks or many years.</p>

<p>From July 2014 our membership options evolved to include a lower membership fee for members who prefer to receive Rattitude in digital format. Rattitude is still available as a print publication and members who would like their own printed copy can receive this for a print subscription surcharge.</p>

<p>If you would like to suggest features that would be of interest to include in a forthcoming issue of Rattitude, or are willing to write an article or supply photographs/line drawings, rescue stories, tips, product reviews, etc, please contact our <a href="mailto:editor@neratsociety.co.uk" target="_new">newsletter editor</a>.</p>
	
<p><strong>You can now read a past issue by clicking the following link: <a href="/resources/docs/rattitude-issue-94.pdf" target="_new">Rattitude - Issue 94</a> [PDF].</strong></p>

<p>In December 2013, Rattitude featured on BBC's "Have I Got News For You" as the publication in the 'Missing Words round'.</p>
